/* Create Tables */

CREATE TABLE JSP.BBS
(
	ID  NOT NULL,
	USERNAME varchar2(10 char),
	TITLE nvarchar2(50) NOT NULL,
	CONTENT nvarchar2(2000) NOT NULL,
	HITCOUNT  DEFAULT 0,
	POSTDATE date DEFAULT SYSDATE,
	CONSTRAINT SYS_C009757 PRIMARY KEY (ID)
